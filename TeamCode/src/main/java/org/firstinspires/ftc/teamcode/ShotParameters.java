package org.firstinspires.ftc.teamcode;

public class ShotParameters {
    public double angle;
    public double topMotorPower;
    public double bottomMotorPower;

    public ShotParameters(double angle, double topMotorPower, double bottomMotorPower) {
        this.angle = angle;
        this.topMotorPower = topMotorPower;
        this.bottomMotorPower = bottomMotorPower;
    }
}
